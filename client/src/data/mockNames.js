export const getRandomNames = (gender, culture, count = 5, feeling = null) => {
  const genderKey = gender || 'male';
  const cultureKey = culture || 'christian';
  
  let availableNames = mockNames[genderKey]?.[cultureKey] || mockNames.male.christian;
  
  // Filter by feeling if specified
  if (feeling && feeling !== 'unique') {
    const filtered = availableNames.filter(name => name.feeling === feeling);
    if (filtered.length > 0) {
      availableNames = filtered;
    }
  }
  
  // Shuffle and return requested count
  const shuffled = [...availableNames].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, Math.min(count, shuffled.length));
}; 